import componentForm from './files/form.vue'
import componentList from './files/list.vue'


// ·
export default {
    props: {
        cloudModule: {
            type: String,
            required: true
        },
        cloudId: {
            required: true
        },
        active: {
            type: Boolean,
            default: true
        },
        translationsFileTypesPaths: {
            type: Array,
            default: ()=>{
                return []
            }
        },
        acceptedFileExtensions: {
            type: Array,
            default: null
        },
        showNewFileTab: {
            type: Boolean,
            default: true
        },
        showListFilesTab: {
            type: Boolean,
            default: true
        },
        showCardHeader: {
            type: Boolean,
            default: true
        }
    },

    components: {
        'component-form': componentForm,
        'component-list': componentList
    },

    data() {
        return {
            show: false,
            translations: {
                core: I18n.t('core.shared')
            },
            active_tab: 0
        }
    },

    mounted() {
        this.setSubscriptions()
        this.setTranslations()
    },

    beforeDestroy(){
        this.deleteSubscriptions()
    },

    methods: {
        setSubscriptions(){
            this.bus.subscribe('show:/module/app/files', () => this.show = !this.show )
        },

        setTranslations(){
            if(this.translationsFileTypesPaths){
                for(let translation_path of this.translationsFileTypesPaths) {
                    this.$set(
                        this.translations,
                        'file_types',
                        {
                            ...this.translations.file_types,
                            ...I18n.t(translation_path)
                        }
                    )
                }
            }
        },

        deleteSubscriptions(){
            this.bus.$off('show:/module/app/files')
        },

        switchToList(){
            this.active_tab = 1
        }
    }
}
</script>
<template>
    <div class="card">
        <div v-if="showCardHeader" class="card-header">
            <div class="card-header-title is-shadowless">
                <h4 class=" title is-4">
                    {{ translations.core.view_text_files }}
                </h4>
            </div>
        </div>
        <div class="card-content">
            <b-tabs expanded v-model="active_tab">
                <b-tab-item :label="translations.core.view_tab_title_files_new" v-if="showNewFileTab">
                    <component-form
                        :cloud-module="cloudModule"
                        :cloud-id="cloudId"
                        :active="active"
                        @upload-complete="switchToList"
                        :accepted-file-extensions="acceptedFileExtensions"
                        :translations="translations"
                    />
                </b-tab-item>
                <b-tab-item :label="translations.core.view_tab_title_files_list" v-if="showListFilesTab">
                    <component-list
                        :cloud-module="cloudModule"
                        :cloud-id="cloudId"
                        :active="active"
                        :translations="translations"
                    />
                </b-tab-item>
            </b-tabs>
        </div>
    </div>
</template>
