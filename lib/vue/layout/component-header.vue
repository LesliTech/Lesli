                    <div class="navbar-item">
                        <slot></slot>
                    </div>
                </div>
            </div>
        </nav>

        <div v-if="shortcuts.length > 0" class="component-header-shortcuts buttons">
            <a 
                class="button"
                v-for="shortcut in shortcuts" :key="shortcut.id"
                :href="shortcut.url" >
