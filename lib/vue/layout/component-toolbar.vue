<script>
export default {
    props: {
        searchText: {
            default: "Search..."
        },

        initialValue: {
            default: null
        }
    },
    mounted(){
        this.setInitialValue()
    },
    data() {
        return {
            timer: null,
            text: ""
        }
    },
    methods: {
        search() {
            clearTimeout(this.timer)
            this.timer = setTimeout(() => this.$emit("search", this.text), 800)
        },
        setInitialValue(){
            if(this.initialValue){
                this.text = this.initialValue
            }
        },
        clearSearch(){
            this.text = ''
            this.$emit("search", this.text)
        }
    }
}
</script>
<template>
    <div class="component-toolbar columns is-multiline">
        <div class="column is-12">
            <div class="field is-grouped component-toolbar">
                <div class="control is-expanded has-icons-right">
                    <input type="text" name="search" :placeholder="searchText" v-model="text" @input="search()" class="input is-shadowless">
                    <span class="icon is-right">
                        <a class="delete" @click="clearSearch"></a>
                    </span>
                </div>
                <slot></slot>
            </div>
        </div>
        <div v-if="$slots['second_row']" class="second-row">
            <slot name="second-row"></slot>
        </div>
        <div v-if="$slots['controls']" class="column is-12">
            <div class="field is-grouped is-justify-content-center">
                <slot name="controls"></slot>
            </div>
        </div>
    </div>
</template>
